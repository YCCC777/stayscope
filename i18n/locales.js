// 單一類別 Google Maps 搜尋 URL
function buildMapsUrl(lat, lng, keyword, zoom = 15) {
  const encoded = encodeURIComponent(keyword);
  return `https://www.google.com/maps/search/${encoded}/@${lat},${lng},${zoom}z`;
}

// 多類別合併搜尋（使用 OR 語法，同一張地圖顯示多種地點）
function buildBundleMapsUrl(lat, lng, keywords, zoom = 14) {
  const combined = keywords.map(k => encodeURIComponent(k)).join('+OR+');
  return `https://www.google.com/maps/search/${combined}/@${lat},${lng},${zoom}z`;
}

