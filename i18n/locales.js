// 將國名 / 代碼統一轉為 2 字母 ISO 代碼
function normalizeCountryCode(raw) {
  if (!raw) return null;
  const s = String(raw).trim();
  if (/^[A-Z]{2}$/i.test(s)) return s.toUpperCase();          // 已經是代碼
  return COUNTRY_NAME_TO_CODE[s] || COUNTRY_NAME_TO_CODE[s.toLowerCase()] || null;
}

// ---- Helper 函式 ----

function getUiStrings(langCode) {
  if (!langCode) langCode = 'zh-TW';
  if (UI_STRINGS[langCode]) return UI_STRINGS[langCode];
  // 語言前綴比對（e.g. 'zh-TW' → 精確; 'zh-Hans' → 'zh-CN'; 'ja-JP' → 'ja'）
  const prefix = langCode.split('-')[0];
  // zh-CN / zh-Hans / zh-SG 等簡中變體 → zh-CN；其餘 zh 系列 → zh-TW
  const zhTarget = /^zh-(CN|Hans|SG|MY)/i.test(langCode) ? 'zh-CN' : 'zh-TW';
  const prefixMap = {
    zh: zhTarget, ja: 'ja', ko: 'ko', en: 'en',
    th: 'th', vi: 'vi', id: 'id', ms: 'ms',
    es: 'es', fr: 'fr', de: 'de',
  };
  return UI_STRINGS[prefixMap[prefix]] || UI_STRINGS['en'];
}

function getSearchKeywords(countryCode) {
  if (!countryCode) return SEARCH_KEYWORDS.DEFAULT;
  return SEARCH_KEYWORDS[countryCode.toUpperCase()] || SEARCH_KEYWORDS.DEFAULT;
}

function getCountryInfo(countryCode) {
  if (!countryCode) return null;
  const code = normalizeCountryCode(countryCode);
  if (code && COUNTRY_INFO[code]) return COUNTRY_INFO[code];
  return { name: countryCode, flag: '🌍' };
}

